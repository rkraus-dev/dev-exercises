int powerLevel = 10000;

if (powerLevel >= 9000)
{
    Console.WriteLine("Isekai God – no one can stop you.");
}
else if (powerLevel >= 5000)
{
    Console.WriteLine("S-Rank Adventurer – respected across all kingdoms.");
}
else if (powerLevel >= 1000)
{
    Console.WriteLine("B-Rank – solid, but there's room to grow.");
}
else
{
    Console.WriteLine("F-Rank – even goblins laugh at you.");
}